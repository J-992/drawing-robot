var scale=0.5;

function lol(x0,y0, x1,y1) {
    x0+=x; x0*=scale;
    y0+=y; y0*=scale;
    x1+=x; x1*=scale;
    y1+=y; y1*=scale;
    line(1-x0/100,y0/100,
         1-x1/100,y1/100);
}

